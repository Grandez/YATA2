DROP TABLE  IF EXISTS CAMERAS CASCADE;
CREATE TABLE CAMERAS  (
    CAMERA   VARCHAR(32)  NOT NULL 
   ,DESCR    VARCHAR(64)  NOT NULL  
   ,EXCHANGE INTEGER      NOT NULL 
   ,ACTIVE   TINYINT      DEFAULT 1 
   ,TOKEN    VARCHAR(255)
   ,USR      VARCHAR(64) 
   ,PWD      VARCHAR(64) 
   ,CC       VARCHAR(512)       
   ,PRIMARY KEY ( CAMERA )
);
