DROP TABLE  IF EXISTS SESSION;
CREATE TABLE SESSION  (
    ID        INTEGER       NOT NULL
   ,SYMBOL    VARCHAR(64)   NOT NULL
   ,PRICE     DOUBLE   
   ,VOLUME    DOUBLE   
   ,RANK      INTEGER     DEFAULT 1001 -- Required to filter
   ,TOKEN     TINYINT     DEFAULT 0
   ,VAR01     DOUBLE
   ,VAR24     DOUBLE
   ,VAR07     DOUBLE
   ,VAR30     DOUBLE
   ,VAR60     DOUBLE
   ,VAR90     DOUBLE
   ,VOL24     DOUBLE
   ,VOL07     DOUBLE
   ,VOL30     DOUBLE
   ,DOMINANCE DOUBLE
   ,TURNOVER  DOUBLE
   ,UPDATED   TIMESTAMP    NOT NULL COMMENT 'Timestamp for this thicker'
   ,TMS       TIMESTAMP    DEFAULT   CURRENT_TIMESTAMP
                           ON UPDATE CURRENT_TIMESTAMP   
   ,PRIMARY KEY (ID,     TMS DESC)
);
